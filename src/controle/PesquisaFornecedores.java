    private void CarregaFornecedores(){

        String SQL;

        SQL = "select (cnpjcpf) as codigo, (nomefornecedor) as nome from t_fornecedor ";

        if (optCnpjCpf.isSelected()){
            SQL = SQL + "where cnpjcpf = '" + jtfPesquisa.getText() + "'";
            optNome.setSelected(false);
        }else{
            SQL = SQL + "where UPPER(nomefornecedor) like '%" + jtfPesquisa.getText().toUpperCase() + "%'";
            optCnpjCpf.setSelected(false);
        }

        Conexao = new ConexaoAccess();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        jTable1.updateUI();

        ResultSet RS = Conexao.Consultar();

        try{

            while (RS.next()){
                DefaultTableModel Tabela = (DefaultTableModel) jTable1.getModel();
                Object[] CarLinha = {RS.getString("codigo"),RS.getString("nome")};
                Tabela.addRow(CarLinha);
            }

        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }

        Conexao.Desconectar();

    }

