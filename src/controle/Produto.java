    public void centralizar(JDesktopPane dp) {
        Dimension ds = dp.getSize();
        Dimension dw = this.getSize();
        this.setLocation((ds.width - dw.width) / 2, (ds.height - dw.height) / 2);
    }

    private void Limpar(){

        jtfCodigo.setText("");
        jtfNome.setText("");

        cboMedida.removeAllItems();
        JogarMedidas();

        jtpDescricao.setText("");
    }

    private void JogarMedidas(){

        cboMedida.removeAllItems();

        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            String SQL;

            SQL = "select (codunidade) as codigo, (unidade) as nome from unidade ";
            SQL = SQL + "where exclusao = 0 ";
            SQL = SQL + "order by unidade ";

            Conexao.setSQL(SQL);
            ResultSet RS = Conexao.Consultar();
            DefaultComboBoxModel Modelo = new DefaultComboBoxModel();
            while(RS.next()){
                Combo CB = new Combo(RS.getString("codigo"),RS.getString("nome"));
                Modelo.addElement(CB);
            }
            cboMedida.setModel(Modelo);
        } catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }

    }

    public void CarregaDados(int Codigo){

        String SQL;

        SQL = "select tpr.codigo, tpr.nome, tpr.descricao, tpr.unidade_cod, uni.unidade ";
        SQL = SQL + "from t_produto tpr ";
        SQL = SQL + "inner join unidade uni on uni.codunidade = tpr.unidade_cod ";
        SQL = SQL + "where tpr.codigo = " + Codigo + " ";
        SQL = SQL + "and tpr.exclusao = 0 ";
        
        Conexao = new ConexaoAccess();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ResultSet RS = Conexao.Consultar();

        try{
            if (RS.next()){

                jtfCodigo.setText(RS.getString("codigo"));
                jtfNome.setText(RS.getString("nome"));

                Combo Mot = new Combo(RS.getString("unidade_cod"), RS.getString("unidade"));
                ComboBoxModel CBM;
                CBM = cboMedida.getModel();
                CBM.setSelectedItem(Mot);

                jtpDescricao.setText("descricao");

                jtfNome.requestFocus();

            }
        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }

    }

