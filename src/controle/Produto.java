    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        Conexao = new ConexaoAccess();
        Conexao.Conectar();

        if (jtfCodigo.getText().equals("")){
            jtfCodigo.setText("0");
        }

        String sSQL = "select * from t_produto where codigo = " + jtfCodigo.getText() + " ";
        Conexao.setSQL(sSQL);
        ResultSet RS = Conexao.Consultar();

        String Medida = ((Combo)cboMedida.getSelectedItem()).getKey();

        try {
            if(RS.next()){
                String SQL;

                SQL = "update t_produto set nome = '" + jtfNome.getText() + "', descricao = '" + jtpDescricao.getText() + "', ";
                SQL = SQL + "unidade_cod = " + Medida + " ";
                SQL = SQL + "where codigo = " + jtfCodigo.getText();

                Conexao.setSQL(SQL);
                Conexao.Executar();
            } else {
                String SQL;
                String Proximo;

                Fun = new Funcoes();
                Proximo = Fun.Proximo("t_produto");

                SQL = "insert into t_produto (codigo, nome, descricao, unidade_cod, exclusao) values (";
                SQL = SQL + Proximo + ", '" + jtfNome.getText() + "', '" + jtpDescricao.getText() + "', ";
                SQL = SQL + Medida + ", 0)";

                Conexao.setSQL(SQL);
                Conexao.Executar();
            }
        } catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "(Erro na validação): " + Erro);
        }

        Conexao.Desconectar();

        Limpar();

        jtfNome.requestFocus();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

