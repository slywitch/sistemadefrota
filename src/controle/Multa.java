    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        Conexao = new ConexaoAccess();
        Conexao.Conectar();

        if (jtfCodigo.getText().equals("")){
            jtfCodigo.setText("0");
        }

        String sSQL = "select * from t_lancamento_multa where codigo = " + jtfCodigo.getText() + " ";
        Conexao.setSQL(sSQL);
        ResultSet RS = Conexao.Consultar();

        String Motorista = ((Combo)cboMotorista.getSelectedItem()).getKey();
        String Veiculo = ((Combo)cboVeiculo.getSelectedItem()).getKey();
        String Multa = ((Combo)cboMulta.getSelectedItem()).getKey();

        try {

            Fun = new Funcoes();
            String Data;

            if (!(Fun.isValidDate(jtfData.getText()))){
                Data = null;
            } else {
                Data = "'" + jtfData.getText() + "'";
            }

            if(RS.next()){
                String SQL;

                SQL = "update t_lancamento_multa set data = " + Data + ", cpf_cod = '" + Motorista + "', ";
                SQL = SQL + "chassis_cod = '" + Veiculo + "', multa_cod = '" + Multa + "' ";
                SQL = SQL + "where codigo = " + jtfCodigo.getText();

                Conexao.setSQL(SQL);
                Conexao.Executar();
            } else {
                String SQL;
                String Proximo;
                
                Fun = new Funcoes();
                Proximo = Fun.Proximo("t_lancamento_multa");

                SQL = "insert into t_lancamento_multa (codigo, data, cpf_cod, chassis_cod, multa_cod, exclusao) values (";
                SQL = SQL + Proximo + ", " + Data + ", '" + Motorista + "', '" + Veiculo + "', '";
                SQL = SQL + Multa + "', 0)";

                Conexao.setSQL(SQL);
                Conexao.Executar();
            }
        } catch (Exception Erro) {
            JOptionPane.showMessageDialog(null, "(Erro na validação): " + Erro);
        }

        Conexao.Desconectar();

        Limpar();

        jtfData.requestFocus();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

