    private void DadosInfracoes(){

        String Multa = ((Combo)cboMulta.getSelectedItem()).getKey();

        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();

            String SQL;

            SQL = "select competencia, pontuacao, ufir, peso from t_multa where codigo = '";
            SQL = SQL + Multa + "' and exclusao = 0 ";

            Conexao.setSQL(SQL);
            ResultSet RS = Conexao.Consultar();

            if (RS.next()){
                jlbCompetencia1.setText(RS.getString("competencia"));

                Fun = new Funcoes();
                jlbGravidade1.setText(Fun.GravidadeMulta(Integer.parseInt(RS.getString("pontuacao"))));
                
                Fun = new Funcoes();

                int Ufir;
                int Peso;
                Double ValorUfir;

                Ufir = Integer.parseInt(RS.getString("ufir"));
                Peso = Integer.parseInt(RS.getString("peso"));
                ValorUfir = Fun.ValorUfir(Ufir, Peso);

                jlbValor1.setText(NumberFormat.getCurrencyInstance().format(ValorUfir));
            }
        }catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }

    }

    private String NivelMulta(int codigo){

        String NM=null;

        Fun = new Funcoes();
        NM = Fun.GravidadeMulta(codigo);

        return NM;

    }

    private double PrecoMulta(int ufir, int peso){

        double preco;

        Fun = new Funcoes();

        preco = Fun.ValorUfir(ufir, peso);

        return preco;

    }

