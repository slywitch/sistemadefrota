    private void jtfCodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfCodigoKeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){

            String SQL;

            SQL = "select tlm.codigo, to_char(tlm.data,'DD/MM/YYYY') as datamulta, tlm.cpf_cod, (tmo.nome) as NomeMotorista, ";
            SQL = SQL + "tlm.chassis_cod, tve.placa, ";
            SQL = SQL + "tlm.multa_cod, tpm.descricao, tpm.competencia, tpm.pontuacao, tpm.ufir, tpm.peso ";
            SQL = SQL + "from t_lancamento_multa tlm ";
            SQL = SQL + "inner join t_motorista tmo on tmo.cpf = tlm.cpf_cod ";
            SQL = SQL + "inner join t_veiculo tve on tve.chassis = tlm.chassis_cod ";
            SQL = SQL + "inner join t_multa tpm on tpm.codigo = tlm.multa_cod ";
            SQL = SQL + "where tlm.codigo = " + jtfCodigo.getText() + " ";
            SQL = SQL + "and tlm.exclusao = 0 ";

            Conexao = new ConexaoAccess();
            Conexao.Conectar() ;
            Conexao.setSQL(SQL);

            ResultSet RS = Conexao.Consultar();

            try{
                if (RS.next()){

                    jtfCodigo.setText(RS.getString("codigo"));
                    jtfData.setText(RS.getString("datamulta"));

                    Combo Mot = new Combo(RS.getString("cpf_cod"), RS.getString("NomeMotorista"));
                    ComboBoxModel CBM;
                    CBM = cboVeiculo.getModel();
                    CBM.setSelectedItem(Mot);

                    Combo Vei = new Combo(RS.getString("chassis_cod"), RS.getString("placa"));
                    ComboBoxModel CBV;
                    CBV = cboVeiculo.getModel();
                    CBV.setSelectedItem(Vei);

                    Combo Mul = new Combo(RS.getString("multa_cod"), RS.getString("descricao"));
                    ComboBoxModel CB;
                    CB = cboMulta.getModel();
                    CB.setSelectedItem(Mul);

                    jlbGravidade1.setText(NivelMulta(Integer.parseInt(RS.getString("pontuacao"))));
                    jlbCompetencia1.setText("competencia");

                    int ufir = Integer.parseInt(RS.getString("ufir"));
                    int peso = Integer.parseInt(RS.getString("peso"));

                    jlbValor1.setText(NumberFormat.getCurrencyInstance().format(PrecoMulta(ufir, peso)));

                    jtfData.requestFocus();
                    
                }
            }catch (Exception Erro){
                JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
            }
        }
    }//GEN-LAST:event_jtfCodigoKeyPressed

