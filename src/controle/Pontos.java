    private void DadosPonto(){

        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();

            String SQL;

            SQL = "select (tmo.nome) as nomemotorista, (sum(tpm.gravidade)) as ponto ";
            SQL = SQL + "from t_lancamento_multa tlm ";
            SQL = SQL + "inner join t_motorista tmo on tmo.cpf = tlm.cpf_cod ";
            SQL = SQL + "inner join t_pontuacao_multas tpm on tpm.codigo = tlm.multa_cod ";
            SQL = SQL + "where tlm.exclusao = 0 ";
            SQL = SQL + "group by tmo.nome ";

            Conexao.setSQL(SQL);
            ResultSet RS = Conexao.Consultar();

            while(RS.next()){
                DefaultTableModel Tabela = (DefaultTableModel) jtbRanking.getModel();
                Object[] CarLinha = {RS.getString("nomemotorista") ,RS.getString("ponto")};
                Tabela.addRow(CarLinha);
            }
        } catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }

    }

