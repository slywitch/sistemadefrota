    private void JogarFornecedor(){
        
        cboFornecedor.removeAllItems();
        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();

            String SQL;

            SQL = "select (cnpjcpf) as codigo, (nomefantasia) as nome ";
            SQL = SQL + "from t_fornecedor ";
            SQL = SQL + "where cnpjcpf in (select fornecedor_cod from t_nota_fiscal) ";
            SQL = SQL + "order by nome ";

            Conexao.setSQL(SQL);
            ResultSet RS = Conexao.Consultar();
            DefaultComboBoxModel Modelo = new DefaultComboBoxModel();
            while(RS.next()){
                Combo CB = new Combo(RS.getString("codigo"),RS.getString("nome"));
                Modelo.addElement(CB);
            }
            cboFornecedor.setModel(Modelo);
        } catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }
        
    }
    
    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        
        try {

            String SQL;
            String Fornecedor;

            Fornecedor = ((Combo)cboFornecedor.getSelectedItem()).getKey();;

            SQL = "select tfo.cnpjcpf, tfo.nomefornecedor, tfo.nomefantasia, tfo.enderecofornecedor, tfo.bairrofornecedor, ";
            SQL = SQL + "cid.cidade, tfo.cepfornecedor, tfo.fone1fornecedor, ";
            SQL = SQL + "tfo.fone2fornecedor, tfo.faxfornecedor, tfo.iefornecedor, tfo.imfornecedor,tfo.contatofornecedor, ";
            SQL = SQL + "tfo.emailfornecedor ";
            SQL = SQL + "from t_fornecedor tfo ";
            SQL = SQL + "inner join cidade cid on cid.codcidade = tfo.cidadecod ";
            

            boolean checked = chkTodos.isSelected();

            if (!(checked)) {
                SQL = SQL + "where tfo.cnpjcpf = '" + Fornecedor + "' ";
                SQL = SQL + "order by tfo.nomefornecedor ";
            }

            Conexao.setSQL(SQL);

            JRResultSetDataSource jrRS = new JRResultSetDataSource(Conexao.Consultar());
            InputStream is = getClass().getResourceAsStream("/reports/fornecedor.jasper");

            JasperPrint print = JasperFillManager.fillReport(is, new HashMap(), jrRS);
            JasperViewer viewer = new JasperViewer(print, false);

            viewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            viewer.setVisible(true);

        } catch (Exception Erro) {
            JOptionPane.showMessageDialog(null, "Erro na busca: " + Erro);
        }

    }//GEN-LAST:event_btnImprimirActionPerformed


