    private void CarregaProdutos(){

        String SQL;

        SQL = "select (cpf) as codigo, nome from t_motorista ";
        
        if (optCpf.isSelected()){
            SQL = SQL + "where cpf = '" + jtfPesquisa.getText() + "' ";
        }else{
            SQL = SQL + "where UPPER(nome) like '%" + jtfPesquisa.getText().toUpperCase() + "%' ";
        }
        
        Conexao = new ConexaoAccess();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        jTable1.updateUI();

        ResultSet RS = Conexao.Consultar();

        try{

            while (RS.next()){
                DefaultTableModel Tabela = (DefaultTableModel) jTable1.getModel();
                Object[] CarLinha = {RS.getString("codigo"),RS.getString("nome")};
                Tabela.addRow(CarLinha);
            }

        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }

        Conexao.Desconectar();

    }

