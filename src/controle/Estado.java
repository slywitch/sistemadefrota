    private void jbtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtExcluirActionPerformed

        Object[] botoes = { "Sim", "Não" };

        int resposta;

        resposta = JOptionPane.showOptionDialog(null, "Você deseja realmente excluir este registro?", "Confirma exclusão", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE, null, botoes, botoes[0]);

        if(resposta == JOptionPane.YES_OPTION){

            String SQL = "update cidade_uf set exclusao = 1 where coduf = '" + jtfCodigo.getText() + "'";

            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            Conexao.setSQL(SQL);
            Conexao.Executar();
            Conexao.Desconectar();

            Limpar();

        }

        jtfNome.requestFocus();

    }//GEN-LAST:event_jbtExcluirActionPerformed

    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        boolean Op;
        String SQL;
        
        String Codigo;
        
        Fun = new Funcoes();
        Codigo = Fun.Proximo("coduf", "cidade_uf", "");

        if (jtfCodigo.getText().equals("")){
            jtfCodigo.setText(Codigo);
        }
        
        Conexao = new ConexaoAccess();
        Conexao.Conectar();
        SQL = "select * from cidade_uf where coduf = " + jtfCodigo.getText() + " ";
        Conexao.setSQL(SQL);
        ResultSet RS = Conexao.Consultar();

        try {
            if(RS.next()){                
                Conexao.setSQL("update cidade_uf set uf = '" + jtfNome.getText().toUpperCase() + "', abreviacao = '" + jtfSigla.getText().toUpperCase() + "' where coduf = " + jtfCodigo.getText());
                Conexao.Executar();
            } else {
                Conexao.setSQL("insert into cidade_uf (coduf, uf, abreviacao, exclusao) values(" + jtfCodigo.getText() + ", '" + jtfNome.getText().toUpperCase() + "', '" + jtfSigla.getText().toUpperCase() + "', 0)");
                Conexao.Executar();
            }

        } catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }
        
        System.out.println(SQL);
        
        Limpar();

        jtfNome.requestFocus();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

