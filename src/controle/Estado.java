    public void centralizar(JDesktopPane dp) {
        Dimension ds = dp.getSize();
        Dimension dw = this.getSize();
        this.setLocation((ds.width - dw.width) / 2, (ds.height - dw.height) / 2);
    }

    public void Limpar(){
        
        jtfCodigo.setText("");
        jtfNome.setText("");
        jtfSigla.setText("");
        
    }
    
    public void CarregaDados(int Codigo){

        String SQL;
        
        SQL = "select * from cidade_uf where coduf = " + Codigo + " ";
        SQL = SQL + "and exclusao = 0 ";
        
        Conexao = new ConexaoAccess();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ResultSet RS = Conexao.Consultar();

        try{
            if (RS.next()){
                jtfCodigo.setText(RS.getString("coduf"));
                jtfNome.setText(RS.getString("uf"));
                jtfSigla.setText(RS.getString("abreviacao"));
            }else{
                Limpar();

                jtfNome.requestFocus();
            }
        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação" + Erro);
        }
        jtfNome.requestFocus();
        Conexao.Desconectar();

    }


