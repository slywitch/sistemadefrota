    public void CarregarDados(String Codigo){

        String SQL;

        SQL = "select descricao, ctb, infrator from t_pontuacao_multas where codigo = '" + Codigo + "' ";
        SQL = SQL + "and exclusao = 0 ";

        try{

            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            Conexao.setSQL(SQL);

            ResultSet RS = Conexao.Consultar();

            if (RS.next()){
                jtfCtb.setText(RS.getString("ctb"));
                jtfInfrator.setText(RS.getString("infrator"));
                jtpDescricao.setText(RS.getString("descricao"));
            }

        }catch(Exception e){
         JOptionPane.showMessageDialog(null,e.getMessage(),"Atenção",JOptionPane.WARNING_MESSAGE);
        }

        Conexao.Desconectar();

    }

    private void Impressao(String Codigo){

        try {

            String SQL;

            SQL = "select to_char(data, 'DD/MM/YYYY') as data, tmo.nome, tve.placa, tpm.codigo, tpm.descricao, tpm.gravidade, ";
            SQL = SQL + "tpm.ufir ";
            SQL = SQL + "from t_lancamento_multa tlm ";
            SQL = SQL + "inner join t_motorista tmo on tmo.cpf = tlm.cpf_cod ";
            SQL = SQL + "inner join t_veiculo tve on tve.chassis = tlm.chassis_cod ";
            SQL = SQL + "inner join t_pontuacao_multas tpm on tpm.codigo = tlm.multa_cod ";
            SQL = SQL + "where tmo.cpf = '" + CodigoMotorista + "' ";
            SQL = SQL + "and tlm.codigo = " + Codigo + " ";
            SQL = SQL + "and tlm.exclusao = 0 ";
            SQL = SQL + "order by nome ";

            System.out.println(SQL);

            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            Conexao.setSQL(SQL);

            JRResultSetDataSource jrRS = new JRResultSetDataSource(Conexao.Consultar());
            InputStream is = getClass().getResourceAsStream("/reports/pontuacaomotorista.jasper");

            JasperPrint print = JasperFillManager.fillReport(is, new HashMap(), jrRS);
            JasperViewer viewer = new JasperViewer(print, false);

            viewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            viewer.setVisible(true);

        } catch (Exception Erro) {
            JOptionPane.showMessageDialog(null, "Erro na busca: " + Erro.getMessage());
            System.out.println(Erro);
        }

    }

