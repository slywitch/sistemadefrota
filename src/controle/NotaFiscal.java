    private void JogarFornecedor(){
        cboFornecedor.removeAllItems();
        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            Conexao.setSQL("select (cnpjcpf) as codigo, (nomefantasia) as nome from t_fornecedor where exclusao = 0 order by nome ");
            ResultSet RS = Conexao.Consultar();
            DefaultComboBoxModel Modelo = new DefaultComboBoxModel();
            while(RS.next()){
                Combo cboF = new Combo(RS.getString("codigo"),RS.getString("nome"));
                Modelo.addElement(cboF);
            }
            cboFornecedor.setModel(Modelo);
        } catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }
    }

    private void JogarProduto(){
        cboProduto.removeAllItems();
        try {
            Conexao = new ConexaoAccess();
            Conexao.Conectar();
            Conexao.setSQL("select codigo, nome from t_produto where exclusao = 0 order by nome ");
            ResultSet RS = Conexao.Consultar();
            DefaultComboBoxModel Modelo = new DefaultComboBoxModel();
            while(RS.next()){
                Combo cboP = new Combo(RS.getString("codigo"),RS.getString("nome"));
                Modelo.addElement(cboP);
            }
            cboProduto.setModel(Modelo);
        } catch(Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro: " + Erro);
        }
    }

    private void Limpar(){

        jtfCodigo.setText("");
        jtfData.setText("");

        cboFornecedor.removeAllItems();
        JogarFornecedor();

        jtpObs.setText("");

        cboProduto.removeAllItems();
        JogarProduto();

        jtfQtde.setText("");
        jtfPreco.setText("");

        ((DefaultTableModel) jTable1.getModel()).setNumRows(0);
        jTable1.updateUI();

    }

    private void Add(){

        cboProduto.removeAllItems();
        JogarProduto();

        jtfQtde.setText("");
        jtfPreco.setText("");

        cboProduto.requestFocus();

    }


