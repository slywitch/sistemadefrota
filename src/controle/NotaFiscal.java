    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        Conexao = new ConexaoAccess();
        Conexao.Conectar();

        if (jtfCodigo.getText().equals("")){
            jtfCodigo.setText("0");
        }

        String sSQL = "select * from t_nota_fiscal where codigo = '" + jtfCodigo.getText() + "' ";

        Conexao.setSQL(sSQL);
        ResultSet RS = Conexao.Consultar();

        String Fornecedor = ((Combo)cboFornecedor.getSelectedItem()).getKey();
        String Produto = ((Combo)cboProduto.getSelectedItem()).getKey();

        try {
            if(!(RS.next())){
                String SQL;

                Fun = new Funcoes();
                String Data;

                if (!(Fun.isValidDate(jtfData.getText()))){
                    Data = null;
                } else {
                    Data = jtfData.getText();
                }


                SQL = "insert into t_nota_fiscal (codigo, fornecedor_cod, data, exclusao) values ('";
                SQL = SQL + jtfCodigo.getText() + "', " + Fornecedor + ", " + Data + ", 0)";

                Conexao.setSQL(SQL);
                Conexao.Executar();

                int linha_total = jTable1.getRowCount();

                int contador = 1;

                while(contador <= linha_total){
                    int linha = contador - 1;

                    String coluna0 = jTable1.getValueAt(linha, 0).toString();
                    //String coluna1 = jTable1.getValueAt(linha, 1).toString();
                    String coluna2 = jTable1.getValueAt(linha, 2).toString();
                    String coluna3 = jTable1.getValueAt(linha, 3).toString();

                    String Proximo;

                    Fun = new Funcoes();
                    Proximo = Fun.Proximo("t_nota_fiscal_item");

                    SQL = "insert into t_nota_fiscal_item (codigo, nf_cod, produto_cod, qtde, preco, exclusao) values (";
                    SQL = SQL + Proximo + ", '" + jtfCodigo.getText() + "', " + coluna0 + ", " + coluna2 + ", " + coluna3 + ", 0)";

                    Conexao.setSQL(SQL);
                    Conexao.Executar();

                    contador++;

                }

            }
        } catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "(Erro na validação): " + Erro);
        }

        Conexao.Desconectar();

        Limpar();

        jtfCodigo.requestFocus();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

