    public void CarregaDados(int Codigo){

        String SQL;

        SQL = "select tmo.codigo, tmo.marca_cod, (tma.nome) as marca, (tmo.nome) as modelo ";
        SQL = SQL + "from t_modelo tmo ";
        SQL = SQL + "inner join t_marca tma on tma.codigo = tmo.marca_cod ";
        SQL = SQL + "where tmo.codigo = " + Codigo + " ";
        SQL = SQL + "and tmo.exclusao = 0 ";

        Conexao = new ConexaoAccess();
        Conexao.Conectar() ;
        Conexao.setSQL(SQL);

        ResultSet RS = Conexao.Consultar();

        try{
            if (RS.next()){
                jtfCodigo.setText(RS.getString("codigo"));
                jtfNome.setText(RS.getString("modelo"));

                Combo TV = new Combo(RS.getString("marca_cod"), RS.getString("marca"));
                ComboBoxModel CMB;
                CMB = cboMarca.getModel();
                CMB.setSelectedItem(TV);
            }else{
                jtfCodigo.setText("");
                jtfNome.setText("");

                cboMarca.removeAllItems();
                JogarNomeMarca();
            }
        }catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }
        jtfNome.requestFocus();
        Conexao.Desconectar();

    }

