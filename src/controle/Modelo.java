    private void jbtSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSalvarActionPerformed

        String strMarca = ((Combo)cboMarca.getSelectedItem()).getKey();

        if (jtfCodigo.getText().equals("")){
            Fun = new Funcoes();
            jtfCodigo.setText(Fun.Proximo("t_modelo"));
        }

        Conexao = new ConexaoAccess();
        Conexao.Conectar();
        Conexao.setSQL("select * from t_modelo where codigo = " + jtfCodigo.getText() + " ");
        ResultSet RS = Conexao.Consultar();

        try {
            if(RS.next()){
                String SQL;
                SQL = "update t_modelo set nome = '" + jtfNome.getText().toUpperCase() + "', marca_cod = " + strMarca + " where codigo = " + jtfCodigo.getText() + "";
                Conexao.setSQL(SQL);
                Conexao.Executar();
            } else {
                String SQL;
                SQL = "insert into t_modelo (codigo, marca_cod, nome, exclusao) values (" + jtfCodigo.getText() + ", " + strMarca + ", '" + jtfNome.getText().toUpperCase() + "', 0)";
                Conexao.setSQL(SQL);
                Conexao.Executar();
            }

        } catch (Exception Erro){
            JOptionPane.showMessageDialog(null, "Erro na validação: " + Erro);
        }

        Conexao.Desconectar();

        jtfCodigo.setText("");
        jtfNome.setText("");

        cboMarca.removeAllItems();
        JogarNomeMarca();

        jtfNome.requestFocus();
        
    }//GEN-LAST:event_jbtSalvarActionPerformed

